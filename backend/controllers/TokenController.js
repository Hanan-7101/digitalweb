let web3 = new Web3(infuraUrl);
const myContract = new web3.eth.Contract(abi, contractAddress);

const getdata = async (req, res, next) => {
  const networkId = await web3.eth.net.getId();
  const tx = myContract.methods.transfer(
    "0xe37ea092a715e873a0f9df001b2675f52d88a68a",
    "1000000000000000000"
  );
  // const gas = await tx.estimateGas({ from: ownerAddress });
  const gas = 300000;
  const gasPrice = await web3.eth.getGasPrice();
  const data = tx.encodeABI();
  const nonce = await web3.eth.getTransactionCount(ownerAddress);

  const signedTx = await web3.eth.accounts.signTransaction(
    {
      to: myContract.options.address,
      data,
      gas,
      gasPrice,
      nonce,
      chainId: networkId,
    },
    privateKey
  );

  const receipt = await web3.eth.sendSignedTransaction(signedTx.rawTransaction);
  console.log(receipt.transactionHash, "transaction");
};

const getBalance = async (req, res, next) => {
  const networkId = await web3.eth.net.getId();
  let balance = await myContract.methods.MAX_PROUDLIONS().call();
  return res.send({ balance });
};

